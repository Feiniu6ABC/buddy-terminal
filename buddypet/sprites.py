HAT_LINES = {
    "none": "", "crown": "   \\^^^/    ", "tophat": "   [___]    ",
    "propeller": "    -+-     ", "halo": "   (   )    ",
    "wizard": "    /^\\     ", "beanie": "   (___)    ", "tinyduck": "    ,>      ",
}


def render_sprite(comp, frame=0):
    frames = BODIES[comp["species"]]
    body = [l.replace("{E}", comp["eye"]) for l in frames[frame % len(frames)]]
    lines = list(body)
    if comp["hat"] != "none":
        lines.insert(0, HAT_LINES[comp["hat"]])
    return lines


def render_face(comp):
    """窄屏单行表情"""
    e = comp["eye"]
    faces = {
        "duck": f"({e}>", "goose": f"({e}>", "blob": f"({e}{e})",
        "cat": f"={e}ω{e}=", "dragon": f"<{e}~{e}>", "octopus": f"~({e}{e})~",
        "owl": f"({e})({e})", "penguin": f"({e}>)", "turtle": f"[{e}_{e}]",
        "snail": f"{e}(@)", "ghost": f"/{e}{e}\\", "axolotl": f"}}{e}.{e}{{",
        "capybara": f"({e}oo{e})", "cactus": f"|{e}  {e}|", "robot": f"[{e}{e}]",
        "rabbit": f"({e}..{e})", "mushroom": f"|{e}  {e}|", "chonk": f"({e}.{e})",
    }
    return faces.get(comp["species"], f"({e}{e})")
